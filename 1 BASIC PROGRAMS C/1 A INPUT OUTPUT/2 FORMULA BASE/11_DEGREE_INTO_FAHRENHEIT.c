#include <stdio.h>
int main()
{
    int degree, fahrenheit;

    printf("\t Program to convert degree into fahrenheit\n");
    printf("Enter degree = : ");
    scanf("%d", &degree);
    fahrenheit = degree * 9.0 / 5.0 + 32;
    printf("\n\n FAHRENHEIT = %d", fahrenheit);
    return 0;

    // Coded by Mayur
}